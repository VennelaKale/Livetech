package basicprograms;

public class ExRelationalOprators {
public static void main(String[] args) {
	int a=78;
	int b=90;
	//lessthan operator
	System.out.println(" a<b:"+(a<b));
	//lessthan or equalto operator
	System.out.println("a<=b: "+(a<=b));
	//greaterthan
	System.out.println("a>b:"+(a>b));
	//greaterthan or equalto
	System.out.println("a>=b:"+(a>=b));
	//equalto
	System.out.println("a==b"+(a==b));
	//Not equal to
	System.out.println("a!=b:"+(a!=b));
}
}
